
public class sayingTenToTwenty {
	public String conversion(int x)
	 {
		 String tenToTwentyWord = "";
		 switch (x)
		 {
		 case 10:
			 tenToTwentyWord = "Ten";
			 break;
		 case 11:
			 tenToTwentyWord = "Eleven";
			 break;
		 case 12:
			 tenToTwentyWord = "Twelve";
			 break;
		 case 13:
			 tenToTwentyWord = "Thirteen";
			 break;
		 case 14:
			 tenToTwentyWord = "Fourteen";
			 break;
		 case 15:
			 tenToTwentyWord = "Fixteen";
			 break;
		 case 16:
			 tenToTwentyWord = "Sixteen";
			 break;
		 case 17:
			 tenToTwentyWord = "Seventeen";
			 break;
		 case 18:
			 tenToTwentyWord = "Eighteen";
			 break;
		 case 19:
			 tenToTwentyWord = "Nineteen";
			 break;
		 	 
}
		 return tenToTwentyWord;
	 }
}
